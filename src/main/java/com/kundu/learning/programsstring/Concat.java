package com.kundu.learning.programsstring;

public class Concat {

    public static void main(String[] args) {

        String str1 = "Be";
        String str2 = "Happy";

        // using concat() funtion
        System.out.println("String concatenation with concat() is " +str1.concat(str2));

        //using + operator
        System.out.println("String concatenation using + operator is " +str1+str2);
    }

}
